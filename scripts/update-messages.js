
const fs = require('fs');
const path = require('path');

const messagesDir = path.join(process.cwd(), 'messages');
const files = fs.readdirSync(messagesDir);

const aboutContent = {
    "description": "Pocket Dive is a platform designed to make scientific knowledge accessible to everyone.",
    "motivation1": "We believe that science should be open and understandable.",
    "motivation2": "Our goal is to bridge the gap between researchers and the public.",
    "solution1": "By using AI, we summarize complex papers into easy-to-read formats.",
    "solution2": "We provide real-time access to the latest research and news.",
    "logoNotice": "Logo generated by AI."
};

files.forEach(file => {
    if (file.endsWith('.json')) {
        const filePath = path.join(messagesDir, file);
        try {
            const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));
            content.About = aboutContent;
            fs.writeFileSync(filePath, JSON.stringify(content, null, 4));
            console.log(`Updated ${file}`);
        } catch (e) {
            console.error(`Error processing ${file}:`, e);
        }
    }
});
